    rus_loss_fn = RUSLoss()
    optimizer = AdamW(model.parameters(), lr=1e-4, weight_decay=1e-5)
    scheduler = CosineAnnealingLR(optimizer, T_max=num_epochs, eta_min=1e-6)

    # Restore optimizer and scheduler if resuming
    if checkpoint_path.exists():
        optimizer.load_state_dict(checkpoint['optimizer_state_dict'])
        scheduler.load_state_dict(checkpoint['scheduler_state_dict'])

    # ========================================================================
    # TRAINING LOOP
    # ========================================================================
    for epoch in range(start_epoch, num_epochs + 1):
        train_metrics = train_epoch(model, train_loader, optimizer, dice_ce_loss,
                                    rus_loss_fn, device, True, epoch)
        val_metrics = validate_epoch(model, val_loader, dice_ce_loss, rus_loss_fn,
                                     device, True, epoch)
        scheduler.step()

        # Print progress
        print(f"Epoch {epoch}/{num_epochs} - Train: L={train_metrics['loss']:.4f}, D={train_metrics['dice']:.4f} | "
              f"Val: D={val_metrics['dice']:.4f}, S={val_metrics['sensitivity']:.4f}, P={val_metrics['precision']:.4f}")

        # Update history
        history['train_loss'].append(train_metrics['loss'])
        history['train_dice'].append(train_metrics['dice'])
        for k in ['val_dice', 'val_sensitivity', 'val_specificity', 'val_precision', 'val_iou']:
            history[k].append(val_metrics[k.replace('val_', '')])

        # Save best model
        if val_metrics['dice'] > best_dice:
            best_dice = val_metrics['dice']
            torch.save({
                'epoch': epoch,
                'model_state_dict': model.state_dict(),
                'dice': best_dice,
                'metrics': val_metrics,
            }, best_model_path)
            print(f"  ✅ New best model saved! Dice: {best_dice:.4f}")

        # ====================================================================
        # SAVE CHECKPOINT EVERY EPOCH (for resumption)
        # ====================================================================
        torch.save({
            'epoch': epoch,
            'model_state_dict': model.state_dict(),
            'optimizer_state_dict': optimizer.state_dict(),
            'scheduler_state_dict': scheduler.state_dict(),
            'best_dice': best_dice,
        }, checkpoint_path)

        # Save history as JSON
        with open(history_path, 'w') as f:
            json.dump(history, f)

        print(f"  💾 Checkpoint saved (epoch {epoch})")

    print(f"\n✅ Training complete! Best Dice: {best_dice:.4f}")
    return model, history

