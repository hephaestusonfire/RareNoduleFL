    # ========================================================================
    # CHECKPOINT RESUMPTION LOGIC
    # ========================================================================
    start_epoch = 1
    best_dice = 0.0
    history = {'val_dice': [], 'val_sensitivity': [], 'val_specificity': [],
               'val_precision': [], 'val_iou': [], 'train_dice': [], 'train_loss': []}

    # Try to load checkpoint
    if checkpoint_path.exists():
        print(f"\n🔄 Found checkpoint! Resuming from: {checkpoint_path}")
        checkpoint = torch.load(checkpoint_path)
        model.load_state_dict(checkpoint['model_state_dict'])
        start_epoch = checkpoint['epoch'] + 1
        best_dice = checkpoint['best_dice']

        # Load history if exists
        if history_path.exists():
            with open(history_path, 'r') as f:
                history = json.load(f)

        print(f"✅ Resumed from epoch {checkpoint['epoch']}")
        print(f"   Best Dice so far: {best_dice:.4f}")
        print(f"   Continuing from epoch {start_epoch}/{num_epochs}")
