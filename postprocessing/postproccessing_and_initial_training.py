def load_nodule_sizes(excel_path: str) -> Dict[str, float]:
    """
    Load nodule size information from LIDC-IDRI Excel file.

    Args:
        excel_path: Path to lidc-idri-nodule-counts-6-23-2015.xlsx

    Returns:
        Dictionary mapping (patient_id, nodule_id) -> diameter_mm
    """
    print(f"\n📊 Loading nodule size data from Excel...")
    print(f"   Path: {excel_path}")

    try:
        # Read Excel file
        df_excel = pd.read_excel(excel_path)

        print(f"   ✅ Loaded Excel with shape: {df_excel.shape}")
        print(f"   📋 Columns: {list(df_excel.columns)}")

        # Create mapping dictionary
        # Adjust column names based on actual Excel structure
        nodule_sizes = {}

        # Common column name variations in LIDC-IDRI files
        patient_col = None
        nodule_col = None
        diameter_col = None

        # Find correct column names (case-insensitive search)
        for col in df_excel.columns:
            col_lower = str(col).lower()
            if 'patient' in col_lower or 'pid' in col_lower or 'subject' in col_lower:
                patient_col = col
            elif 'nodule' in col_lower and 'no' in col_lower:
                nodule_col = col
            elif 'diameter' in col_lower or 'size' in col_lower:
                diameter_col = col

        if patient_col and nodule_col and diameter_col:
            print(f"   🔍 Found columns: Patient='{patient_col}', Nodule='{nodule_col}', Diameter='{diameter_col}'")

            for _, row in df_excel.iterrows():
                patient_id = str(row[patient_col])
                nodule_id = f"nodule-{int(row[nodule_col])}"
                diameter = float(row[diameter_col])

                # Ensure patient_id format matches (LIDC-IDRI-XXXX)
                if not patient_id.startswith('LIDC-IDRI'):
                    patient_id = f"LIDC-IDRI-{patient_id.zfill(4)}"

                key = (patient_id, nodule_id)
                nodule_sizes[key] = diameter

            print(f"   ✅ Loaded {len(nodule_sizes)} nodule size entries")

            # Statistics
            diameters = list(nodule_sizes.values())
            small_count = sum(1 for d in diameters if d < 10.0)
            large_count = sum(1 for d in diameters if d >= 10.0)

            print(f"   📈 Statistics:")
            print(f"      • Small nodules (<10mm): {small_count} ({small_count/len(diameters)*100:.1f}%)")
            print(f"      • Large nodules (≥10mm): {large_count} ({large_count/len(diameters)*100:.1f}%)")
            print(f"      • Mean diameter: {np.mean(diameters):.2f}mm")
            print(f"      • Median diameter: {np.median(diameters):.2f}mm")
            print(f"      • Min diameter: {np.min(diameters):.2f}mm")
            print(f"      • Max diameter: {np.max(diameters):.2f}mm")

        else:
            print(f"   ⚠️  Could not find standard columns. Available columns:")
            print(f"      {list(df_excel.columns)}")
            print(f"   ℹ️  Please check the Excel file structure.")

            # Show first few rows
            print(f"\n   First 5 rows:")
            print(df_excel.head())

        return nodule_sizes

    except Exception as e:
        print(f"   ⚠️  Error loading Excel: {e}")
        print(f"   ℹ️  Will use default classification (all nodules as small)")
        return {}


