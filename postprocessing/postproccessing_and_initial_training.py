class RUSLoss(nn.Module):
    def __init__(self, weight=0.1, num_bins=10):
        super().__init__()
        self.weight = weight
        self.num_bins = num_bins

    def compute_entropy(self, probs):
        return -torch.sum(probs * torch.log(probs + 1e-8), dim=1)

    def compute_ece(self, probs, targets):
        confidences, predictions = torch.max(probs, dim=1)
        targets_binary = (targets.squeeze(1) > 0.5).float()
        confidences_flat = confidences.flatten()
        predictions_flat = predictions.flatten().float()
        targets_flat = targets_binary.flatten()
        bin_boundaries = torch.linspace(0, 1, self.num_bins + 1, device=probs.device)
        ece = 0.0
        for i in range(self.num_bins):
            in_bin = (confidences_flat > bin_boundaries[i]) & (confidences_flat <= bin_boundaries[i + 1])
            if in_bin.sum() > 0:
                accuracy_in_bin = (predictions_flat[in_bin] == targets_flat[in_bin]).float().mean()
                avg_confidence_in_bin = confidences_flat[in_bin].mean()
                ece += torch.abs(avg_confidence_in_bin - accuracy_in_bin) * in_bin.sum().float()
        return ece / confidences_flat.numel()

    def forward(self, probs, targets, is_small):
        entropy = self.compute_entropy(probs)
        ece = self.compute_ece(probs, targets)
        is_small_mask = is_small.float().unsqueeze(1).unsqueeze(2).expand_as(entropy)
        return (is_small_mask * entropy).mean() + ece * self.weight

def compute_metrics(preds, targets, threshold=0.5):
    preds_binary = (torch.sigmoid(preds) > threshold).float()
    targets_binary = (targets > 0.5).float()
    preds_flat = preds_binary.view(-1)
    targets_flat = targets_binary.view(-1)
    tp = (preds_flat * targets_flat).sum()
    fp = (preds_flat * (1 - targets_flat)).sum()
    fn = ((1 - preds_flat) * targets_flat).sum()
    tn = ((1 - preds_flat) * (1 - targets_flat)).sum()
    return {
        'dice': ((2 * tp + 1e-8) / (2 * tp + fp + fn + 1e-8)).item(),
        'sensitivity': ((tp + 1e-8) / (tp + fn + 1e-8)).item(),
        'specificity': ((tn + 1e-8) / (tn + fp + 1e-8)).item(),
        'precision': ((tp + 1e-8) / (tp + fp + 1e-8)).item(),
        'iou': ((tp + 1e-8) / (tp + fp + fn + 1e-8)).item(),
    }

def train_epoch(model, dataloader, optimizer, dice_ce_loss, rus_loss, device, use_rus, epoch):
    model.train()
    total_loss = total_dice = total_sens = 0.0
    num_batches = 0
    for batch in tqdm(dataloader, desc=f"Epoch {epoch} [Train]"):
        images = batch['image'].to(device)
        masks = batch['mask'].to(device)
        is_small = batch['is_small'].to(device)
        optimizer.zero_grad()
        logits = model(images)
        loss_dice_ce = dice_ce_loss(logits, masks)
        loss_rus = 0.0
        if use_rus:
            probs = torch.softmax(torch.cat([1 - torch.sigmoid(logits), torch.sigmoid(logits)], dim=1), dim=1)
            loss_rus = rus_loss(probs, masks, is_small)
        total_loss_batch = loss_dice_ce + loss_rus
        total_loss_batch.backward()
        optimizer.step()
        metrics = compute_metrics(logits, masks)
        total_loss += total_loss_batch.item()
        total_dice += metrics['dice']
        total_sens += metrics['sensitivity']
        num_batches += 1
    return {'loss': total_loss/num_batches, 'dice': total_dice/num_batches, 'sensitivity': total_sens/num_batches}

