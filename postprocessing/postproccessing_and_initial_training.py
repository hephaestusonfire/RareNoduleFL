class FederatedDataSplitter:
    """
    Split preprocessed LIDC-IDRI data for federated learning experiments.

    Supports:
    - IID: Random stratified split across clients
    - Non-IID: Skewed by nodule size (small vs large)
    - Heterogeneous: Different noise levels per client
    """

    def __init__(
        self,
        csv_path: str,
        data_dir: str,
        excel_path: str,
        num_clients: int = 5,
        test_size: float = 0.2,
        random_seed: int = 42,
    ):
        """
        Args:
            csv_path: Path to dataset_ready.csv from preprocessing
            data_dir: Path to directory with NPY files
            excel_path: Path to LIDC-IDRI Excel file with nodule sizes
            num_clients: Number of federated clients (default 5)
            test_size: Fraction of data for global test set (default 0.2)
            random_seed: Random seed for reproducibility
        """
        self.csv_path = Path(csv_path)
        self.data_dir = Path(data_dir)
        self.num_clients = num_clients
        self.test_size = test_size
        self.random_seed = random_seed

        # Load nodule sizes from Excel
        self.nodule_sizes = load_nodule_sizes(excel_path)

        # Load dataset
        print(f"\n📂 Loading CSV from: {csv_path}")
        self.df = pd.read_csv(csv_path)
        print(f"📊 Loaded dataset: {len(self.df)} samples from {self.df['patient_id'].nunique()} patients")

        # Update is_small column based on Excel data
        self._update_nodule_sizes()

    def _update_nodule_sizes(self):
        """
        Update is_small column in dataframe based on Excel nodule sizes.
        Small nodules: diameter < 10mm
        Large nodules: diameter >= 10mm
        """
        print(f"\n🔄 Updating nodule size classifications...")

        if not self.nodule_sizes:
            print(f"   ⚠️  No Excel data available. Using default (all as small nodules)")
            self.df['is_small'] = 1
            return

        updated_count = 0
        small_count = 0
        large_count = 0

        for idx, row in self.df.iterrows():
            patient_id = row['patient_id']
            nodule_id = row['nodule_id']

            key = (patient_id, nodule_id)

            if key in self.nodule_sizes:
                diameter = self.nodule_sizes[key]
                is_small = 1 if diameter < 10.0 else 0
                self.df.at[idx, 'is_small'] = is_small
                updated_count += 1

                if is_small:
                    small_count += 1
                else:
                    large_count += 1

        print(f"   ✅ Updated {updated_count}/{len(self.df)} samples")
        print(f"   📊 Classification results:")
        print(f"      • Small nodules (<10mm): {small_count} ({small_count/len(self.df)*100:.1f}%)")
        print(f"      • Large nodules (≥10mm): {large_count} ({large_count/len(self.df)*100:.1f}%)")

        # Save updated CSV
        updated_csv_path = self.csv_path.parent / "dataset_ready_with_sizes.csv"
        self.df.to_csv(updated_csv_path, index=False)
        print(f"   💾 Saved updated CSV to: {updated_csv_path}")

    def split_iid(self) -> Tuple[List[pd.DataFrame], pd.DataFrame]:
        """
        IID split: Random stratified distribution across clients.

        Returns:
            Tuple of (client_dfs, test_df)
        """
        print("\n🔄 Creating IID split...")

        # Split train/test globally (stratified by patient)
        unique_patients = self.df['patient_id'].unique()
        train_patients, test_patients = train_test_split(
            unique_patients,
            test_size=self.test_size,
            random_state=self.random_seed,
        )

        train_df = self.df[self.df['patient_id'].isin(train_patients)].copy()
        test_df = self.df[self.df['patient_id'].isin(test_patients)].copy()

        print(f"   Train: {len(train_df)} samples ({len(train_patients)} patients)")
        print(f"   Test: {len(test_df)} samples ({len(test_patients)} patients)")

        # Split train data equally across clients (random)
        train_df = train_df.sample(frac=1, random_state=self.random_seed).reset_index(drop=True)

        client_dfs = []
        samples_per_client = len(train_df) // self.num_clients

        for i in range(self.num_clients):
            start_idx = i * samples_per_client
            if i == self.num_clients - 1:
                # Last client gets remaining samples
                client_df = train_df.iloc[start_idx:].copy()
            else:
                client_df = train_df.iloc[start_idx:start_idx + samples_per_client].copy()

            client_dfs.append(client_df)
            small_ratio = (client_df['is_small'] == 1).sum() / len(client_df) * 100
            print(f"   Client {i}: {len(client_df)} samples ({small_ratio:.1f}% small)")

        return client_dfs, test_df

    def split_non_iid(self, alpha: float = 0.5) -> Tuple[List[pd.DataFrame], pd.DataFrame]:
        """
        Non-IID split: Skewed by nodule size (small vs large).
        Each client has different distribution of small/large nodules.

        Args:
            alpha: Dirichlet concentration parameter (lower = more skewed)
                   0.1 = highly non-IID, 1.0 = moderately non-IID

        Returns:
            Tuple of (client_dfs, test_df)
        """
        print(f"\n🔄 Creating Non-IID split (α={alpha})...")

        # Split train/test globally
        unique_patients = self.df['patient_id'].unique()
        train_patients, test_patients = train_test_split(
            unique_patients,
            test_size=self.test_size,
            random_state=self.random_seed,
        )

        train_df = self.df[self.df['patient_id'].isin(train_patients)].copy()
        test_df = self.df[self.df['patient_id'].isin(test_patients)].copy()

        print(f"   Train: {len(train_df)} samples ({len(train_patients)} patients)")
        print(f"   Test: {len(test_df)} samples ({len(test_patients)} patients)")

        # Separate by nodule size
        small_nodules = train_df[train_df['is_small'] == 1].reset_index(drop=True)
        large_nodules = train_df[train_df['is_small'] == 0].reset_index(drop=True)

        print(f"   Small nodules (<10mm): {len(small_nodules)}")
        print(f"   Large nodules (≥10mm): {len(large_nodules)}")

        # Use Dirichlet distribution for non-IID split
        np.random.seed(self.random_seed)

        # Proportions for small/large nodules across clients
        small_proportions = np.random.dirichlet([alpha] * self.num_clients)
        large_proportions = np.random.dirichlet([alpha] * self.num_clients)

        # Shuffle data
        small_nodules = small_nodules.sample(frac=1, random_state=self.random_seed).reset_index(drop=True)
        large_nodules = large_nodules.sample(frac=1, random_state=self.random_seed).reset_index(drop=True)

        # Distribute to clients
        client_dfs = []
        small_start = 0
        large_start = 0

        for i in range(self.num_clients):
            # Calculate how many samples for this client
            small_count = int(small_proportions[i] * len(small_nodules))
            large_count = int(large_proportions[i] * len(large_nodules))

            # Handle last client (get remaining)
            if i == self.num_clients - 1:
                small_count = len(small_nodules) - small_start
                large_count = len(large_nodules) - large_start

            # Slice data
            client_small = small_nodules.iloc[small_start:small_start + small_count]
            client_large = large_nodules.iloc[large_start:large_start + large_count]

            client_df = pd.concat([client_small, client_large], ignore_index=True)
            client_df = client_df.sample(frac=1, random_state=self.random_seed + i).reset_index(drop=True)

            client_dfs.append(client_df)

            small_ratio = len(client_small) / len(client_df) * 100 if len(client_df) > 0 else 0
            print(f"   Client {i}: {len(client_df)} samples ({small_ratio:.1f}% small nodules)")

            small_start += small_count
            large_start += large_count

        return client_dfs, test_df

    def create_dataloaders(
        self,
        client_dfs: List[pd.DataFrame],
        test_df: pd.DataFrame,
        batch_size: int = 16,
        num_workers: int = 2,
        heterogeneous_noise: bool = True,
    ) -> Tuple[List[DataLoader], DataLoader]:
        """
        Create DataLoaders for FL training.

        Args:
            client_dfs: List of DataFrames, one per client
            test_df: Global test DataFrame
            batch_size: Batch size for training
            num_workers: Number of workers for data loading
            heterogeneous_noise: If True, each client gets different noise level

        Returns:
            Tuple of (client_loaders, test_loader)
        """
        print("\n🔧 Creating DataLoaders...")

        client_loaders = []

        for i, client_df in enumerate(client_dfs):
            # Heterogeneous noise: different sigma per client
            if heterogeneous_noise:
                noise_sigma = 10.0 + i * 5.0  # 10, 15, 20, 25, 30 HU
            else:
                noise_sigma = 15.0  # Standard σ=15HU

            dataset = LIDCNoduleDataset(
                data_df=client_df,
                data_dir=self.data_dir,
                is_train=True,
                augment_rare=True,  # Apply rare nodule boosting
                client_noise_sigma=noise_sigma,
            )

            loader = DataLoader(
                dataset,
                batch_size=batch_size,
                shuffle=True,
                num_workers=num_workers,
                pin_memory=True,
                drop_last=False,
            )

            client_loaders.append(loader)
            print(f"   Client {i} DataLoader: {len(dataset)} samples, noise σ={noise_sigma:.1f}HU")

        # Global test loader (no augmentation)
        test_dataset = LIDCNoduleDataset(
            data_df=test_df,
            data_dir=self.data_dir,
            is_train=False,
            augment_rare=False,
        )

        test_loader = DataLoader(
            test_dataset,
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=True,
        )

        print(f"   Test DataLoader: {len(test_dataset)} samples")

        return client_loaders, test_loader


