class LIDCNoduleDataset(Dataset):
    """
    PyTorch Dataset for LIDC-IDRI lung nodule NPY files with augmentation.

    Features:
    - Loads preprocessed NPY files (images + masks)
    - Applies medical imaging augmentations (flip, rotate, scale, noise)
    - Supports heterogeneous augmentations per client
    - Implements rare nodule boosting
    """

    def __init__(
        self,
        data_df: pd.DataFrame,
        data_dir: Path,
        transform: Optional[Compose] = None,
        is_train: bool = True,
        augment_rare: bool = False,
        client_noise_sigma: float = 15.0,  # Poisson noise σ=15HU
    ):
        """
        Args:
            data_df: DataFrame with columns ['fname', 'patient_id', 'nodule_id', 'is_small', 'label']
            data_dir: Path to directory containing NPY files
            transform: MONAI Compose transform pipeline
            is_train: If True, apply augmentations
            augment_rare: If True, oversample small nodules (<10mm)
            client_noise_sigma: Heterogeneous noise level per client
        """
        self.data_dir = Path(data_dir)
        self.is_train = is_train
        self.client_noise_sigma = client_noise_sigma

        # Apply rare nodule boosting (duplicate small nodules x3)
        if augment_rare and is_train:
            rare_nodules = data_df[data_df['is_small'] == 1]
            # Duplicate rare samples 2 additional times (3x total)
            data_df = pd.concat([data_df, rare_nodules, rare_nodules], ignore_index=True)
            print(f"✅ Rare nodule boosting: {len(rare_nodules)} small nodules duplicated 3x")

        self.data_df = data_df.reset_index(drop=True)

        # Set up transforms
        if transform is None:
            self.transform = self._get_default_transforms(is_train)
        else:
            self.transform = transform

        print(f"📊 Dataset initialized: {len(self.data_df)} samples")

    def _get_default_transforms(self, is_train: bool) -> Compose:
        """
        Create default MONAI transform pipeline.
        Augmentations: flip/rotate/scale + Poisson noise σ=15HU + contrast ±15%
        """
        if is_train:
            # Training augmentations
            transforms = [
                # Geometric augmentations
                RandFlipd(keys=["image", "mask"], prob=0.5, spatial_axis=0),
                RandFlipd(keys=["image", "mask"], prob=0.5, spatial_axis=1),
                RandRotate90d(keys=["image", "mask"], prob=0.5, max_k=3),
                RandAffined(
                    keys=["image", "mask"],
                    prob=0.5,
                    rotate_range=(np.pi / 18, np.pi / 18),  # ±10 degrees
                    scale_range=(0.1, 0.1),  # ±10% scale
                    mode=["bilinear", "nearest"],
                    padding_mode="border",
                ),

                # Intensity augmentations (heterogeneous per client)
                RandGaussianNoised(
                    keys=["image"],
                    prob=0.5,
                    mean=0.0,
                    std=self.client_noise_sigma / 255.0,  # Normalize to [0,1] range
                ),
                RandAdjustContrastd(
                    keys=["image"],
                    prob=0.5,
                    gamma=(0.85, 1.15),  # ±15% contrast
                ),

                # Ensure intensity range [0, 1]
                ScaleIntensityd(keys=["image"], minv=0.0, maxv=1.0),

                # Convert to PyTorch tensors
                ToTensord(keys=["image", "mask"]),
            ]
        else:
            # Validation/test: no augmentation
            transforms = [
                ScaleIntensityd(keys=["image"], minv=0.0, maxv=1.0),
                ToTensord(keys=["image", "mask"]),
            ]

        return Compose(transforms)

    def __len__(self) -> int:
        return len(self.data_df)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        """
        Load and return a single sample.

        Returns:
            Dictionary with keys: 'image', 'mask', 'label', 'is_small', 'fname'
        """
        row = self.data_df.iloc[idx]
        fname = row['fname']

        # Load NPY files (matching your preprocessing output format)
        img_path = self.data_dir / f"{fname}_img.npy"
        mask_path = self.data_dir / f"{fname}_mask.npy"

        try:
            image = np.load(img_path).astype(np.float32)  # Shape: (1, H, W)
            mask = np.load(mask_path).astype(np.float32)   # Shape: (1, H, W)
        except Exception as e:
            print(f"⚠️ Error loading {fname}: {e}")
            # Return empty tensors as fallback
            image = np.zeros((1, 256, 256), dtype=np.float32)
            mask = np.zeros((1, 256, 256), dtype=np.float32)

        # Prepare data dict for MONAI transforms
        data_dict = {
            "image": image,
            "mask": mask,
        }

        # Apply transforms
        if self.transform:
            data_dict = self.transform(data_dict)

        # Add metadata
        data_dict["label"] = torch.tensor(row['label'], dtype=torch.long)
        data_dict["is_small"] = torch.tensor(row['is_small'], dtype=torch.float32)
        data_dict["fname"] = fname

        return data_dict


