SMALL_PIX = 200   # mask pixels < 200 ≈ nodule < ~10 mm

class LIDCNoduleDataset(Dataset):
    def __init__(self, data_df, data_dir, is_train=True,
                 augment_rare=False, client_noise_sigma=15.0):
        self.data_dir = Path(data_dir)
        self.sigma    = client_noise_sigma
        if augment_rare and is_train:
            rare     = data_df[data_df["is_small"] == 1]
            data_df  = pd.concat([data_df, rare, rare], ignore_index=True)
        self.data_df  = data_df.reset_index(drop=True)
        self.tfm      = self._tfm(is_train)

    def _tfm(self, is_train):
        # ResizeWithPadOrCropd ensures ALL slices → 256x256 regardless of
        # original NPY size. Some older preprocessed files are 128x128.
        # This prevents the collation crash: "stack expects equal size".
        from monai.transforms import ResizeWithPadOrCropd
        base = [ResizeWithPadOrCropd(keys=["image","mask"],
                                     spatial_size=[256,256],
                                     mode="constant")]
        if is_train:
            return Compose(base + [
                RandFlipd(["image","mask"], prob=0.5, spatial_axis=0),
                RandFlipd(["image","mask"], prob=0.5, spatial_axis=1),
                RandRotate90d(["image","mask"], prob=0.5, max_k=3),
                RandAffined(["image","mask"], prob=0.5,
                            rotate_range=(np.pi/18,np.pi/18),
                            scale_range=(0.1,0.1),
                            mode=["bilinear","nearest"],
                            padding_mode="border"),
                RandGaussianNoised(["image"], prob=0.5,
                                   mean=0.0, std=self.sigma/255.0),
                RandAdjustContrastd(["image"], prob=0.5, gamma=(0.85,1.15)),
                ScaleIntensityd(["image"], minv=0.0, maxv=1.0),
                ToTensord(["image","mask"]),
            ])
        return Compose(base + [
            ScaleIntensityd(["image"],minv=0.0,maxv=1.0),
            ToTensord(["image","mask"])
        ])

    def __len__(self): return len(self.data_df)

    def __getitem__(self, idx):
        row   = self.data_df.iloc[idx]
        fname = row["fname"]
        try:
            image = np.load(self.data_dir/f"{fname}_img.npy").astype(np.float32)
            mask  = np.load(self.data_dir/f"{fname}_mask.npy").astype(np.float32)
        except Exception as e:
            print(f"⚠️ {fname}: {e}")
            image = np.zeros((1,256,256),np.float32)
            mask  = np.zeros((1,256,256),np.float32)
        d = self.tfm({"image":image,"mask":mask})
        # Derive is_small from mask pixels — NOT the broken CSV column
        mp = float((mask > 0.5).sum())
        d["is_small"] = torch.tensor(1.0 if 0<mp<SMALL_PIX else 0.0,
                                      dtype=torch.float32)
        d["label"]    = torch.tensor(row["label"], dtype=torch.long)
        d["fname"]    = fname
        return d

