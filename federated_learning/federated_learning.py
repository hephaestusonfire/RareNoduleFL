class CombinedLoss(nn.Module):
    def __init__(self):
        super().__init__()
        self._l = DiceCELoss(sigmoid=True, include_background=False,
                              to_onehot_y=False, lambda_dice=0.7, lambda_ce=0.3)
    def forward(self, p, t): return self._l(p, t)

class RUSLoss(nn.Module):
    def __init__(self, weight=0.1, num_bins=10):
        super().__init__()
        self.w = weight; self.nb = num_bins
    def _entropy(self, probs):
        return -torch.sum(probs*torch.log(probs+1e-8), dim=1)
    def _ece(self, probs, targets):
        conf,preds = torch.max(probs, dim=1)
        tgt  = (targets.squeeze(1)>0.5).float()
        bins = torch.linspace(0,1,self.nb+1, device=probs.device)
        ece  = torch.tensor(0.0, device=probs.device)
        for i in range(self.nb):
            m = (conf.flatten()>bins[i])&(conf.flatten()<=bins[i+1])
            if m.sum()>0:
                acc = (preds.flatten().float()[m]==tgt.flatten()[m]).float().mean()
                ece += torch.abs(conf.flatten()[m].mean()-acc)*m.sum().float()
        return ece/(conf.flatten().numel()+1e-8)
    def forward(self, probs, targets, is_small):
        ent  = self._entropy(probs)
        ece  = self._ece(probs, targets)
        mask = is_small.float().unsqueeze(1).unsqueeze(2).expand_as(ent)
        return (mask*ent).mean() + ece*self.w

def compute_metrics(preds, targets, thr=0.5):
    pb=(torch.sigmoid(preds)>thr).float(); tb=(targets>0.5).float()
    pf,tf=pb.view(-1),tb.view(-1)
    tp=(pf*tf).sum(); fp=(pf*(1-tf)).sum()
    fn=((1-pf)*tf).sum(); tn=((1-pf)*(1-tf)).sum()
    return {"dice":    ((2*tp+1e-8)/(2*tp+fp+fn+1e-8)).item(),
            "sensitivity":((tp+1e-8)/(tp+fn+1e-8)).item(),
            "specificity":((tn+1e-8)/(tn+fp+1e-8)).item(),
            "precision":  ((tp+1e-8)/(tp+fp+1e-8)).item(),
            "iou":        ((tp+1e-8)/(tp+fp+fn+1e-8)).item()}

def compute_ece_scalar(model, loader):
    model.eval(); ca,aa=[],[]
    with torch.no_grad():
        for batch in loader:
            imgs=batch["image"].to(device); masks=batch["mask"].to(device)
            with autocast(): logits=model(imgs)
            ca.extend(torch.sigmoid(logits).view(-1).cpu().numpy().tolist())
            aa.extend((masks>0.5).float().view(-1).cpu().numpy().tolist())
    c=np.array(ca); a=np.array(aa); ece=0.0
    for i in range(10):
        m=(c>=i/10)&(c<(i+1)/10)
        if m.sum()>0: ece+=abs(c[m].mean()-a[m].mean())*m.sum()
    return float(ece/(len(c)+1e-8))

def collect_predictions(model, loader):
    model.eval(); ps,ts=[],[]
    with torch.no_grad():
        for batch in loader:
            imgs=batch["image"].to(device); masks=batch["mask"].to(device)
            with autocast(): logits=model(imgs)
            ps.extend((torch.sigmoid(logits)>0.5).float().view(-1).cpu().numpy().tolist())
            ts.extend((masks>0.5).float().view(-1).cpu().numpy().tolist())
    return np.array(ps), np.array(ts)

def dice_per_batch(model, loader):
    model.eval(); dices=[]
    with torch.no_grad():
        for batch in loader:
            imgs=batch["image"].to(device); masks=batch["mask"].to(device)
            with autocast(): logits=model(imgs)
            dices.append(compute_metrics(logits,masks)["dice"])
    return dices

