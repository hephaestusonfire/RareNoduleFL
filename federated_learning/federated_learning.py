def mcnemar(pa, pb, tg, la, lb):
    ca=(pa==tg); cb=(pb==tg)
    b=((ca==True )&(cb==False)).sum()
    c=((ca==False)&(cb==True )).sum()
    chi2=(abs(b-c)-1)**2/(b+c+1e-8)
    p=float(scipy_stats.chi2.sf(chi2,df=1))
    sig="✅ p<0.05" if p<0.05 else "❌ ns"
    line=f"  McNemar [{la} vs {lb}]: b={b}, c={c}, χ²={chi2:.3f}, p={p:.4f} {sig}"
    print(line); return line

def ttest(da, db, la, lb):
    n=min(len(da),len(db))
    t,p=scipy_stats.ttest_rel(da[:n],db[:n])
    sig="✅ p<0.05" if p<0.05 else "❌ ns"
    line=f"  t-test [{la} vs {lb}]: t={t:.3f}, p={p:.4f} {sig}"
    print(line); return line

