class NoduleClient(fl.client.NumPyClient):
    """
    Flower federated learning client — LIDC-IDRI nodule segmentation.
    Implements get_parameters / fit / evaluate per Flower spec.
    Used in simulated FL: clients run sequentially on one GPU.
    """
    def __init__(self, cid, trainloader, valloader,
                 use_rus=False, n_local_epochs=2):
        self.cid            = cid
        self.trainloader    = trainloader
        self.valloader      = valloader
        self.use_rus        = use_rus
        self.n_local_epochs = n_local_epochs
        self.model          = UNetModel().to(device)
        self.seg_loss       = CombinedLoss()
        self.rus_loss_fn    = RUSLoss(weight=0.1)

    def get_parameters(self, config):
        return [v.cpu().numpy() for v in self.model.state_dict().values()]

    def set_parameters(self, parameters):
        keys = list(self.model.state_dict().keys())
        sd   = {k: torch.tensor(v) for k,v in zip(keys, parameters)}
        self.model.load_state_dict(sd, strict=True)

    def fit(self, parameters, config):
        self.set_parameters(parameters)
        self.model.train()
        optimizer = AdamW(self.model.parameters(), lr=1e-4, weight_decay=1e-5)
        scaler    = GradScaler()
        ep_dices, ep_losses = [], []
        for ep in range(self.n_local_epochs):
            tot_loss=tot_dice=0.0; n=0
            loader_iter = tqdm(self.trainloader,
                               desc=f"    C{self.cid} ep{ep+1}/{self.n_local_epochs}",
                               leave=False, ncols=80)
            for batch in loader_iter:
                imgs=batch["image"].to(device)
                masks=batch["mask"].to(device)
                is_small=batch["is_small"].to(device)
                optimizer.zero_grad()
                with autocast():
                    logits   = self.model(imgs)
                    loss_seg = self.seg_loss(logits, masks)
                    loss_rus = torch.tensor(0.0, device=device)
                    if self.use_rus:
                        p=torch.sigmoid(logits)
                        probs=torch.softmax(torch.cat([1-p,p],dim=1),dim=1)
                        loss_rus=self.rus_loss_fn(probs,masks,is_small)
                    loss=loss_seg+loss_rus
                scaler.scale(loss).backward()
                scaler.step(optimizer); scaler.update()
                m=compute_metrics(logits.detach(),masks)
                tot_loss+=loss.item(); tot_dice+=m["dice"]; n+=1
                loader_iter.set_postfix(loss=f"{loss.item():.4f}",
                                        dice=f"{m['dice']:.4f}")
            ep_dices.append(tot_dice/max(n,1))
            ep_losses.append(tot_loss/max(n,1))
        metrics={"train_dice":float(np.mean(ep_dices)),
                 "train_loss":float(np.mean(ep_losses)),
                 "client_id": self.cid}
        return self.get_parameters(config={}), len(self.trainloader.dataset), metrics

    def evaluate(self, parameters, config):
        self.set_parameters(parameters)
        self.model.eval()
        tot_loss=tot_dice=tot_sens=0.0; n=0
        with torch.no_grad():
            for batch in self.valloader:
                imgs=batch["image"].to(device); masks=batch["mask"].to(device)
                with autocast():
                    logits=self.model(imgs)
                    loss_val=self.seg_loss(logits,masks)
                m=compute_metrics(logits,masks)
                tot_loss+=loss_val.item(); tot_dice+=m["dice"]
                tot_sens+=m["sensitivity"]; n+=1
        return (float(tot_loss/max(n,1)),
                len(self.valloader.dataset),
                {"dice":float(tot_dice/max(n,1)),
                 "sensitivity":float(tot_sens/max(n,1))})

