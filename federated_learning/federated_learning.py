def fedavg_aggregate(client_sds, client_weights):
    total  = sum(client_weights)
    agg_sd = copy.deepcopy(client_sds[0])
    for key in agg_sd:
        agg_sd[key] = torch.zeros_like(agg_sd[key], dtype=torch.float32)
        for sd,w in zip(client_sds, client_weights):
            agg_sd[key] += sd[key].float()*(w/total)
    return agg_sd

def evaluate_global(model, loader):
    model.eval(); all_m=[]
    with torch.no_grad():
        for batch in loader:
            imgs=batch["image"].to(device); masks=batch["mask"].to(device)
            with autocast(): logits=model(imgs)
            all_m.append(compute_metrics(logits,masks))
    return {k:float(np.mean([m[k] for m in all_m])) for k in all_m[0]}

