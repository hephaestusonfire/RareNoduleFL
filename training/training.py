class LIDCNoduleDataset(Dataset):
    def __init__(self, data_df: pd.DataFrame, data_dir: Path,
                 is_train: bool = True, augment_rare: bool = False,
                 client_noise_sigma: float = 15.0):
        self.data_dir = Path(data_dir)
        self.is_train = is_train
        self.client_noise_sigma = client_noise_sigma

        if augment_rare and is_train:
            rare = data_df[data_df["is_small"] == 1]
            data_df = pd.concat([data_df, rare, rare], ignore_index=True)
            print(f"  ✅ Rare boost: {len(rare)} small nodules × 3")

        self.data_df = data_df.reset_index(drop=True)
        self.transform = self._build_transforms(is_train)
        print(f"  📊 Dataset: {len(self.data_df)} samples  [train={is_train}]")

    def _build_transforms(self, is_train: bool) -> Compose:
        if is_train:
            return Compose([
                RandFlipd(keys=["image","mask"], prob=0.5, spatial_axis=0),
                RandFlipd(keys=["image","mask"], prob=0.5, spatial_axis=1),
                RandRotate90d(keys=["image","mask"], prob=0.5, max_k=3),
                RandAffined(keys=["image","mask"], prob=0.6,
                            rotate_range=(np.pi/12, np.pi/12),
                            scale_range=(0.15, 0.15),
                            translate_range=(10, 10),
                            mode=["bilinear","nearest"],
                            padding_mode="border"),
                RandZoomd(keys=["image","mask"], prob=0.4,
                          min_zoom=0.85, max_zoom=1.15,
                          mode=["bilinear","nearest"]),
                RandGaussianNoised(keys=["image"], prob=0.5, mean=0.0,
                                   std=self.client_noise_sigma / 255.0),
                RandGaussianSmoothd(keys=["image"], prob=0.3,
                                    sigma_x=(0.5, 1.5), sigma_y=(0.5, 1.5)),
                RandAdjustContrastd(keys=["image"], prob=0.5, gamma=(0.80, 1.20)),
                ScaleIntensityd(keys=["image"], minv=0.0, maxv=1.0),
                ToTensord(keys=["image","mask"]),
            ])
        else:
            return Compose([
                ScaleIntensityd(keys=["image"], minv=0.0, maxv=1.0),
                ToTensord(keys=["image","mask"]),
            ])

    def __len__(self): return len(self.data_df)

    def __getitem__(self, idx):
        row      = self.data_df.iloc[idx]
        fname    = row["fname"]
        img_path = self.data_dir / f"{fname}_img.npy"
        msk_path = self.data_dir / f"{fname}_mask.npy"
        try:
            image = np.load(img_path).astype(np.float32)
            mask  = np.load(msk_path).astype(np.float32)
        except Exception as e:
            print(f"⚠️  Load error {fname}: {e}")
            image = np.zeros((1, 256, 256), dtype=np.float32)
            mask  = np.zeros((1, 256, 256), dtype=np.float32)

        d = self.transform({"image": image, "mask": mask})
        d["label"]    = torch.tensor(row["label"], dtype=torch.long)
        # Re-derive is_small from mask pixel count at runtime — do NOT trust the
        # CSV is_small column, which defaults to 1 for ALL samples when the Excel
        # file was missing during preprocessing. A nodule mask covering fewer than
        # 200 pixels (≈ diameter < ~16px ≈ <10mm at typical resolution) is "small".
        mask_pixels   = float((mask > 0.5).sum())
        csv_is_small  = float(row["is_small"])
        # Use mask-derived label; fall back to CSV only when mask is empty (background slice)
        if mask_pixels > 0:
            is_small_val = 1.0 if mask_pixels < 200 else 0.0
        else:
            is_small_val = csv_is_small   # background slice — use CSV
        d["is_small"] = torch.tensor(is_small_val, dtype=torch.float32)
        d["fname"]    = fname
        return d

