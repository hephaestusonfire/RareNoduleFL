# 10. UNIVERSAL RESUME-AWARE TRAINING LOOP
# ============================================================================

def run_training(
    model_class,
    model_name,
    train_loader,
    val_loader,
    seg_loss_class,       # e.g. HybridLoss  or  CombinedLoss
    rus_weight   = 0.1,
    max_epochs   = 200,
    patience     = 30,
    init_lr      = 1e-4,
    weight_decay = 1e-5,
    model_kwargs = None,
    dice_target  = None,   # optional early-exit when target reached
):
    """
    Fully self-contained trainer.
    - Loads latest checkpoint if it exists.
    - Saves checkpoint every epoch, best model when composite score improves.
    - Returns (model, history).
    """
    if model_kwargs is None: model_kwargs = {}

    model = model_class(**model_kwargs).to(device)
    ckpt_path    = MODEL_DIR / f"{model_name}_checkpoint.pth"
    best_path    = MODEL_DIR / f"{model_name}_best.pth"
    history_path = MODEL_DIR / f"{model_name}_history.json"

    # ---- history schema -------------------------------------------------
    hist_keys = ["epochs", "train_loss", "train_dice",
                 "val_dice", "val_rare_dice", "composite", "lr"]
    history   = {k: [] for k in hist_keys}

    start_epoch    = 1
    best_composite = 0.0
    es_counter     = 0

    # ---- load checkpoint ------------------------------------------------
    if ckpt_path.exists():
        print(f"\n🔄 Found checkpoint: {ckpt_path}")
        ckpt     = torch.load(ckpt_path, map_location=device)
        saved_sd = ckpt["model_state_dict"]
        model_sd = model.state_dict()

        # Detect architecture mismatches (different channels/res_units/norm)
        missing    = [k for k in model_sd if k not in saved_sd]
        unexpected = [k for k in saved_sd if k not in model_sd]
        mismatched = [k for k in model_sd if k in saved_sd
                      and saved_sd[k].shape != model_sd[k].shape]

        if missing or unexpected or mismatched:
            print(f"   ⚠️  Architecture mismatch — cannot do full resume:")
            print(f"      Missing keys    : {len(missing)}")
            print(f"      Unexpected keys : {len(unexpected)}")
            print(f"      Shape mismatches: {len(mismatched)}")
            # strict=False still crashes on shape mismatches in PyTorch.
            # Manually keep only keys that exist in both AND have matching shapes.
            compatible_sd = {
                k: v for k, v in saved_sd.items()
                if k in model_sd and v.shape == model_sd[k].shape
            }
            model_sd.update(compatible_sd)
            model.load_state_dict(model_sd, strict=True)
            print(f"   ✅ Transferred {len(compatible_sd)}/{len(model_sd)} compatible weight tensors")
            print(f"   ▸ Epoch counter reset to 1 (architecture change — fresh training from partial weights)")
            # Don't restore epoch or best_composite — incompatible arch means we retrain
        else:
            print(f"   ✅ Architecture matches — full resume")
            model.load_state_dict(saved_sd, strict=True)
            start_epoch    = ckpt.get("epoch", 0) + 1
            best_composite = ckpt.get("best_composite",
                             ckpt.get("best_dice",
                             ckpt.get("best_composite_score", 0.0)))
            # If checkpoint has 0.0 (old training didn't save composite),
            # try to read it from the best.pth which was saved with val_dice
            if best_composite == 0.0 and best_path.exists():
                try:
                    best_ckpt = torch.load(best_path, map_location="cpu")
                    best_composite = best_ckpt.get("composite",
                                    best_ckpt.get("dice",
                                    best_ckpt.get("best_dice", 0.0)))
                    print(f"   ▸ best_composite recovered from best.pth: {best_composite:.4f}")
                except Exception:
                    pass

        if history_path.exists():
            try:
                with open(history_path) as f:
                    loaded = json.load(f)
                # Ensure every required key exists and is a list (not scalar)
                for k in hist_keys:
                    v = loaded.get(k, [])
                    history[k] = list(v) if isinstance(v, (list, tuple)) else []
            except Exception:
                history = {k: [] for k in hist_keys}

        print(f"   ▸ Start epoch          : {start_epoch}")
        print(f"   ▸ Best composite so far: {best_composite:.4f}")
        print(f"   ▸ History epochs loaded: {len(history.get('val_dice', []))}")
    else:
        print(f"\n🆕 Fresh training: {model_name}")

    # ---- init optimiser / scheduler / scaler ----------------------------
    seg_loss    = seg_loss_class()
    rus_loss_fn = RUSLoss(weight=rus_weight)
    optimizer   = AdamW(model.parameters(), lr=init_lr,
                        weight_decay=weight_decay)
    scheduler   = ReduceLROnPlateau(optimizer, mode="max", factor=0.5,
                                    patience=12, verbose=True, min_lr=1e-7)
    scaler      = GradScaler()

    # Only restore optimizer/scheduler when architecture fully matched
    if ckpt_path.exists() and start_epoch > 1:
        # Try to restore optimizer momentum/adam states for warm resume
        try:
            optimizer.load_state_dict(ckpt["optimizer_state_dict"])
            print("   ▸ Optimizer restored ✅")
        except Exception as e:
            # Old checkpoints may have incompatible param group structure.
            # Extract the last saved LR and apply it directly so we don't
            # start cold at init_lr=1e-4 but continue from where training stopped.
            try:
                saved_lr = ckpt["optimizer_state_dict"]["param_groups"][0]["lr"]
            except Exception:
                saved_lr = init_lr
            for pg in optimizer.param_groups:
                pg["lr"] = saved_lr
            print(f"   ⚠️  Optimizer state incompatible ({e})")
            print(f"   ▸ Fresh optimizer with saved LR={saved_lr:.2e} (adam states reset)")

        # Never restore ReduceLROnPlateau state — its internal "best" would poison
        # the scheduler into thinking every epoch is worse than the old best.
        current_lr = optimizer.param_groups[0]["lr"]
        print(f"   ▸ Scheduler reset to fresh state (LR={current_lr:.2e} from optimizer)")
    elif ckpt_path.exists() and start_epoch == 1:
        print("   ▸ Fresh optimizer/scheduler (architecture mismatch or new run)")

    # ---- training loop --------------------------------------------------
    print("=" * 70)
    print(f"🚀 {model_name}  |  Epochs {start_epoch}–{max_epochs}  "
          f"|  patience={patience}  |  bs={BATCH_SIZE}  |  AMP+TTA")
    print(f"   Params: {sum(p.numel() for p in model.parameters()):,}")
    print(f"   Loss  : {seg_loss_class.__name__}  +  RUSLoss(w={rus_weight})")
    if dice_target:
        print(f"   Target: Dice ≥ {dice_target:.2f}")
    print("=" * 70)

    for epoch in range(start_epoch, max_epochs + 1):
        print(f"\n📅 [{model_name}] Epoch {epoch}/{max_epochs}")

        tr = train_epoch(model, train_loader, optimizer, seg_loss,
                         rus_loss_fn, scaler, rus_weight, epoch)
        va = validate_epoch(model, val_loader, seg_loss,
                            rus_loss_fn, rus_weight, epoch)

        composite = 0.7 * va["rare_dice"] + 0.3 * va["dice"]
        lr_now    = optimizer.param_groups[0]["lr"]
        scheduler.step(composite)

        n_rare = va.get("n_rare_samples", 0)
        print(f"   Train  → loss={tr['loss']:.4f}  dice={tr['dice']:.4f}")
        print(f"   Val    → dice={va['dice']:.4f}  rare={va['rare_dice']:.4f}"
              f"  comp={composite:.4f}  lr={lr_now:.2e}  [rare_samples={n_rare}]")

        # update history
        history["epochs"].append(epoch)
        history["train_loss"].append(tr["loss"])
        history["train_dice"].append(tr["dice"])
        history["val_dice"].append(va["dice"])
        history["val_rare_dice"].append(va["rare_dice"])
        history["composite"].append(composite)
        history["lr"].append(lr_now)

        # save best
        if composite > best_composite:
            best_composite = composite
            es_counter     = 0
            torch.save({
                "epoch": epoch, "model_state_dict": model.state_dict(),
                "composite": best_composite, "val_dice": va["dice"],
                "val_rare_dice": va["rare_dice"],
            }, best_path)
            print(f"   ⭐ NEW BEST composite={best_composite:.4f}  saved → {best_path.name}")
        else:
            es_counter += 1
            print(f"   ❗ No improvement ({es_counter}/{patience})")
            if es_counter >= patience:
                print(f"   🛑 Early stop at epoch {epoch}")
                break

        # save checkpoint
        torch.save({
            "epoch":              epoch,
            "model_state_dict":   model.state_dict(),
            "optimizer_state_dict": optimizer.state_dict(),
            "scheduler_state_dict": scheduler.state_dict(),
            "best_composite":     best_composite,
        }, ckpt_path)
        with open(history_path, "w") as f:
            json.dump(history, f, indent=2)

        # target reached?
        if dice_target and va["dice"] >= dice_target:
            print(f"\n🎯 TARGET REACHED: Dice={va['dice']:.4f} ≥ {dice_target:.2f}")
            break

    print(f"\n✅ {model_name} DONE  |  Best composite={best_composite:.4f}")
    return model, history

