# 8. TTA (Test-Time Augmentation)  —  4 deterministic flips/rots
# ============================================================================

def tta_predict(model, images):
    """Average predictions over 4 TTA variants."""
    augs = [
        lambda x: x,
        lambda x: torch.flip(x, [3]),
        lambda x: torch.flip(x, [2]),
        lambda x: torch.rot90(x, k=1, dims=[2, 3]),
    ]
    de_augs = [
        lambda x: x,
        lambda x: torch.flip(x, [3]),
        lambda x: torch.flip(x, [2]),
        lambda x: torch.rot90(x, k=-1, dims=[2, 3]),
    ]
    preds = []
    with torch.no_grad():
        for aug, de_aug in zip(augs, de_augs):
            out = model(aug(images))
            preds.append(de_aug(out))
    return torch.mean(torch.stack(preds, dim=0), dim=0)

