def train_epoch(model, loader, optimizer, seg_loss, rus_loss_fn,
                scaler, rus_weight, epoch):
    model.train()
    tot_loss = tot_dice = 0.0
    n = 0
    for batch in tqdm(loader, desc=f"  Train {epoch}", leave=False):
        imgs     = batch["image"].to(device)
        masks    = batch["mask"].to(device)
        is_small = batch["is_small"].to(device)
        optimizer.zero_grad()
        with autocast():
            logits   = model(imgs)
            loss_seg = seg_loss(logits, masks)
            loss_rus = torch.tensor(0.0, device=device)
            if rus_weight > 0:
                p      = torch.sigmoid(logits)
                probs  = torch.softmax(torch.cat([1-p, p], dim=1), dim=1)
                loss_rus = rus_loss_fn(probs, masks, is_small) * rus_weight
            loss = loss_seg + loss_rus
        scaler.scale(loss).backward()
        scaler.step(optimizer)
        scaler.update()
        m         = compute_metrics(logits.detach(), masks)
        tot_loss += loss.item()
        tot_dice += m["dice"]
        n        += 1
    return {"loss": tot_loss/n, "dice": tot_dice/n}


def validate_epoch(model, loader, seg_loss, rus_loss_fn, rus_weight, epoch):
    model.eval()
    tot_loss = tot_dice = tot_rare = 0.0
    n = rare_n = 0
    with torch.no_grad():
        for batch in tqdm(loader, desc=f"  Val   {epoch}", leave=False):
            imgs     = batch["image"].to(device)
            masks    = batch["mask"].to(device)
            is_small = batch["is_small"].to(device)
            with autocast():
                logits   = tta_predict(model, imgs)
                loss_seg = seg_loss(logits, masks)
                loss_rus = torch.tensor(0.0, device=device)
                if rus_weight > 0:
                    p      = torch.sigmoid(logits)
                    probs  = torch.softmax(torch.cat([1-p, p], dim=1), dim=1)
                    loss_rus = rus_loss_fn(probs, masks, is_small) * rus_weight
                loss = loss_seg + loss_rus
            m         = compute_metrics(logits, masks)
            tot_loss += loss.item()
            tot_dice += m["dice"]
            n        += 1
            rare_idx  = (is_small > 0.5).nonzero(as_tuple=True)[0]
            if len(rare_idx) > 0:
                # Compute per-sample rare dice and accumulate sum + count
                for ri in rare_idx:
                    rm = compute_metrics(
                        logits[ri:ri+1], masks[ri:ri+1]
                    )
                    tot_rare += rm["dice"]
                    rare_n   += 1
    rare_dice = tot_rare / rare_n if rare_n > 0 else 0.0
    return {"loss": tot_loss/n, "dice": tot_dice/n, "rare_dice": rare_dice,
            "n_rare_samples": rare_n}

