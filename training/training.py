class CombinedLoss(nn.Module):
    """Dice-CE 0.7/0.3 — identical to your original."""
    def __init__(self):
        super().__init__()
        self.loss = DiceCELoss(
            sigmoid=True, softmax=False, include_background=False,
            to_onehot_y=False, lambda_dice=0.7, lambda_ce=0.3
        )
    def forward(self, pred, target): return self.loss(pred, target)


class FocalTverskyLoss(nn.Module):
    """
    Focal Tversky Loss — penalises FN more (alpha=0.7, beta=0.3, gamma=0.75).
    Works much better for small / rare nodules than vanilla Dice-CE.
    """
    def __init__(self, alpha=0.7, beta=0.3, gamma=0.75, smooth=1e-6):
        super().__init__()
        self.alpha = alpha   # weight on FN  (recall-boosting)
        self.beta  = beta    # weight on FP
        self.gamma = gamma   # focal exponent
        self.smooth = smooth

    def forward(self, logits, targets):
        probs  = torch.sigmoid(logits)
        tp     = (probs * targets).sum(dim=(2, 3))
        fp     = (probs * (1 - targets)).sum(dim=(2, 3))
        fn     = ((1 - probs) * targets).sum(dim=(2, 3))
        tversky = (tp + self.smooth) / (tp + self.alpha*fn + self.beta*fp + self.smooth)
        focal_tversky = (1 - tversky) ** self.gamma
        return focal_tversky.mean()


class HybridLoss(nn.Module):
    """0.5 * DiceCE + 0.5 * FocalTversky — best of both worlds."""
    def __init__(self):
        super().__init__()
        self.dice_ce = CombinedLoss()
        self.ft      = FocalTverskyLoss()

    def forward(self, pred, target):
        return 0.5 * self.dice_ce(pred, target) + 0.5 * self.ft(pred, target)


class RUSLoss(nn.Module):
    """Rare-Uncertainty-Sensitivity loss — unchanged from your code."""
    def __init__(self, weight=0.1, num_bins=10):
        super().__init__()
        self.weight   = weight
        self.num_bins = num_bins

    def compute_entropy(self, probs):
        return -torch.sum(probs * torch.log(probs + 1e-8), dim=1)

    def compute_ece(self, probs, targets):
        conf, preds = torch.max(probs, dim=1)
        tgt = (targets.squeeze(1) > 0.5).float()
        bins = torch.linspace(0, 1, self.num_bins + 1, device=probs.device)
        ece  = 0.0
        for i in range(self.num_bins):
            m = (conf.flatten() > bins[i]) & (conf.flatten() <= bins[i+1])
            if m.sum() > 0:
                acc  = (preds.flatten().float()[m] == tgt.flatten()[m]).float().mean()
                conf_mean = conf.flatten()[m].mean()
                ece += torch.abs(conf_mean - acc) * m.sum().float()
        return ece / (conf.flatten().numel() + 1e-8)

    def forward(self, probs, targets, is_small):
        entropy      = self.compute_entropy(probs)
        ece          = self.compute_ece(probs, targets)
        is_small_map = is_small.float().unsqueeze(1).unsqueeze(2).expand_as(entropy)
        return (is_small_map * entropy).mean() + ece * self.weight


def compute_metrics(preds, targets, threshold=0.5):
    pb = (torch.sigmoid(preds) > threshold).float()
    tb = (targets > 0.5).float()
    pf, tf = pb.view(-1), tb.view(-1)
    tp = (pf * tf).sum()
    fp = (pf * (1 - tf)).sum()
    fn = ((1 - pf) * tf).sum()
    tn = ((1 - pf) * (1 - tf)).sum()
    return {
        "dice":        ((2*tp + 1e-8) / (2*tp + fp + fn + 1e-8)).item(),
        "sensitivity": ((tp + 1e-8)   / (tp + fn + 1e-8)).item(),
        "specificity": ((tn + 1e-8)   / (tn + fp + 1e-8)).item(),
        "precision":   ((tp + 1e-8)   / (tp + fp + 1e-8)).item(),
        "iou":         ((tp + 1e-8)   / (tp + fp + fn + 1e-8)).item(),
    }

