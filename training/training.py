def evaluate_model(model, model_name, loader):
    """Load best checkpoint and evaluate on test loader with TTA."""
    best_path = MODEL_DIR / f"{model_name}_best.pth"
    if best_path.exists():
        ckpt = torch.load(best_path, map_location=device)
        model.load_state_dict(ckpt["model_state_dict"])
        print(f"  ✅ Loaded best checkpoint: epoch={ckpt.get('epoch','?')} "
              f"composite={ckpt.get('composite', 0):.4f}")
    else:
        print(f"  ⚠️  No best checkpoint found for {model_name}, using current weights")

    model.eval()
    all_metrics = []
    with torch.no_grad():
        for batch in tqdm(loader, desc=f"  Test {model_name}", leave=False):
            imgs  = batch["image"].to(device)
            masks = batch["mask"].to(device)
            with autocast():
                logits = tta_predict(model, imgs)
            m = compute_metrics(logits, masks)
            all_metrics.append(m)

    avg = {k: np.mean([m[k] for m in all_metrics]) for k in all_metrics[0]}
    print(f"\n  🧪 TEST RESULTS — {model_name}")
    for k, v in avg.items():
        print(f"     {k:14s}: {v:.4f}")
    return avg

