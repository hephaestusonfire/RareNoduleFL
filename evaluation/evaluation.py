def compute_gradcam(model,img_tensor,layer_name):
    model.eval()
    extractor=GradCAMpp(model,target_layer=layer_name)
    inp=img_tensor.detach().float().to(device).requires_grad_(True)
    out=model(inp); score=torch.sigmoid(out).mean()
    cam=extractor(class_idx=None,scores=score.unsqueeze(0))
    extractor.remove_hooks()
    if cam is None or (isinstance(cam,list) and (len(cam)==0 or cam[0] is None)):
        return np.zeros((256,256))
    hm=cam[0].squeeze().detach().cpu().numpy() if isinstance(cam,list) else \
       cam.squeeze().detach().cpu().numpy()
    if hm.max()>hm.min(): hm=(hm-hm.min())/(hm.max()-hm.min())
    if hm.shape!=(256,256):
        hm=np.array(Image.fromarray((hm*255).astype(np.uint8))
                     .resize((256,256),Image.BILINEAR))/255.0
    return hm

def overlay_hm(img_np,heatmap,alpha=0.5):
    rgb=np.stack([img_np]*3,axis=2)
    rgb=(rgb*255).astype(np.uint8)
    heat=(plt.cm.jet(heatmap)[:,:,:3]*255).astype(np.uint8)
    return ((1-alpha)*rgb+alpha*heat).astype(np.uint8)

