def entropy_map(model,img_t):
    model.eval()
    inp=img_t.detach().float().to(device)
    with torch.no_grad():
        p=torch.sigmoid(model(inp)).squeeze().cpu().numpy()
    p=np.clip(p,1e-8,1-1e-8)
    return -(p*np.log(p)+(1-p)*np.log(1-p)),p

