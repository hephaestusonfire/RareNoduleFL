    def detect(prob_map,min_px=10):
        binary=(prob_map>0.5).astype(np.uint8)
        labeled,n=ndimage.label(binary); dets=[]; clean=np.zeros_like(binary)
        for cid in range(1,n+1):
            comp=(labeled==cid); area=comp.sum()
            if area<min_px: continue
            dets.append({"score":float(prob_map[comp].max()),"area":int(area),"mask":comp})
            clean|=comp.astype(np.uint8)
        if len(dets)>1:
            best=max(dets,key=lambda d:d["score"])
            clean=best["mask"].astype(np.uint8); dets=[best]
        return clean,dets

    rows=[]
