def mcnemar(pa, pb, tg, la, lb):
    ca = (pa == tg); cb = (pb == tg)
    b  = ((ca == True)  & (cb == False)).sum()
    c  = ((ca == False) & (cb == True )).sum()
    chi2 = (abs(b - c) - 1)**2 / (b + c + 1e-8)
    p    = float(scipy_stats.chi2.sf(chi2, df=1))
    sig  = "SIGNIFICANT (p<0.05)" if p < 0.05 else "not significant"
    return (f"  McNemar [{la} vs {lb}]:\n"
            f"    b={b}  c={c}  χ²={chi2:.3f}  p={p:.4f}  → {sig}")

def ttest_batches(da, db, la, lb):
    n = min(len(da), len(db))
    t, p = scipy_stats.ttest_rel(da[:n], db[:n])
    sig  = "SIGNIFICANT (p<0.05)" if p < 0.05 else "not significant"
    return (f"  Paired t-test [{la} vs {lb}]:\n"
            f"    t={t:.3f}  p={p:.4f}  → {sig}")

