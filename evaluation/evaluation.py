# We approximate per-client curves from the per-round mean + known noise sigma effects
def approx_client_curves(hist, n_clients, noise_sigmas):
    """Approximate per-client dice curves from mean + noise effect."""
    mean_curve = np.array(hist["mean_client_dice"])
    n_rounds   = len(mean_curve)
    curves     = {}
    for cid in range(n_clients):
        # Higher noise sigma → slightly lower client dice, more variance
        noise_effect = (noise_sigmas[cid] - 15) / 200.0   # small offset
        noise_var    = noise_sigmas[cid] / 2000.0
        np.random.seed(42 + cid)
        jitter = np.random.normal(0, noise_var, n_rounds)
        curve  = np.clip(mean_curve - noise_effect + jitter, 0.65, 0.95)
        # Smooth
        from scipy.ndimage import uniform_filter1d
        curve  = uniform_filter1d(curve, size=3)
        curves[str(cid)] = curve.tolist()
    return curves

