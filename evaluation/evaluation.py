def detection_ece(sub_df, n_bins=10):
    pos  = sub_df[sub_df["gt_positive"]==1].copy()
    if len(pos)==0: return 0.0,[],[],[]
    conf = pos["det_score"].values
    acc  = ((pos["pred_positive"]==1)&(pos["iou"]>=0.5)).astype(float).values
    bins = np.linspace(0,1,n_bins+1)
    ece=0.0; bc=[]; ba=[]; bct=[]
    for i in range(n_bins):
        m=(conf>=bins[i])&(conf<bins[i+1])
        if m.sum()==0: continue
        bc.append(float(conf[m].mean()))
        ba.append(float(acc[m].mean()))
        bct.append(int(m.sum()))
        ece+=abs(bc[-1]-ba[-1])*m.sum()
    return float(ece/max(len(pos),1)), bc, ba, bct

def rare_ece(sub_df, rare_thr=0.3):
    pos  = sub_df[sub_df["gt_positive"]==1].copy()
    rare = pos[pos["iou"]<rare_thr]
    def _ece(df):
        if len(df)==0: return 0.0
        c=df["det_score"].values; a=((df["pred_positive"]==1)&(df["iou"]>=0.1)).astype(float).values
        bins=np.linspace(0,1,11); e=0.0
        for i in range(10):
            m=(c>=bins[i])&(c<bins[i+1])
            if m.sum()>0: e+=abs(c[m].mean()-a[m].mean())*m.sum()
        return float(e/max(len(df),1))
    return _ece(rare), len(rare)

